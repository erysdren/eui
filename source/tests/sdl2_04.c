/* button callback */
void my_cool_button_callback(void *user)
{
	EUI_UNUSED(user);
	printf("My cool button was pressed!\n");
}

/* install palette to SDL_Surface */
void install_palette(const char *filename, SDL_Surface *surface)
{
	FILE *file;
	int i;
	SDL_Color colors[16];

	file = fopen(filename, "rb");
	if (!file)
		return;

	for (i = 0; i < 16; i++)
	{
		colors[i].r = fgetc(file);
		colors[i].g = fgetc(file);
		colors[i].b = fgetc(file);
	}

	SDL_SetPaletteColors(surface->format->palette, colors, 0, 16);

	fclose(file);
}

/* main */
int main(int argc, char **argv)
{
	uint32_t format;
	unsigned int rmask, gmask, bmask, amask;
	int bpp;
	eui_vec2_t pos, size;

	EUI_UNUSED(argc);
	EUI_UNUSED(argv);

	/* init */
	SDL_Init(SDL_INIT_VIDEO);

	/* create window */
	window = SDL_CreateWindow("eui",
		SDL_WINDOWPOS_CENTERED, SDL_WINDOWPOS_CENTERED,
		WIDTH, HEIGHT,
		SDL_WINDOW_RESIZABLE | SDL_WINDOW_ALLOW_HIGHDPI
	);

	/* create renderer */
	renderer = SDL_CreateRenderer(window, -1, SDL_RENDERER_PRESENTVSYNC);
	SDL_RenderSetLogicalSize(renderer, WIDTH, HEIGHT);
	SDL_RenderSetIntegerScale(renderer, SDL_TRUE);
	SDL_SetWindowMinimumSize(window, WIDTH, HEIGHT);
	SDL_SetRenderDrawColor(renderer, 0, 0, 0, 255);
	SDL_RenderClear(renderer);
	SDL_RenderPresent(renderer);
	SDL_SetHint(SDL_HINT_RENDER_SCALE_QUALITY, "nearest");

	/* create our render surface */
	surface4 = SDL_CreateRGBSurface(0, WIDTH, HEIGHT, 4, 0, 0, 0, 0);
	SDL_FillRect(surface4, NULL, 0);

	/* install vga 16-color palette */
	install_palette("vga.pal", surface4);

	/* create display surface */
	format = SDL_GetWindowPixelFormat(window);
	SDL_PixelFormatEnumToMasks(format, &bpp, &rmask, &gmask, &bmask, &amask);
	surface32 = SDL_CreateRGBSurface(0, WIDTH, HEIGHT, bpp, rmask, gmask, bmask, amask);
	texture = SDL_CreateTexture(renderer, format, SDL_TEXTUREACCESS_STREAMING, WIDTH, HEIGHT);

	/* make sure relative mouse mode is disabled */
	SDL_SetRelativeMouseMode(SDL_FALSE);

	/* setup blit rect */
	blitrect.x = 0;
	blitrect.y = 0;
	blitrect.w = WIDTH;
	blitrect.h = HEIGHT;

	/* xbm pos */
	pos.x = 0;
	pos.y = 0;

	/* main loop */
	running = SDL_TRUE;
	while (running)
	{
		/* parse sdl events */
		while (SDL_PollEvent(&event))
		{
			eui_push_event_sdl2(&event);

			switch (event.type)
			{
				case SDL_QUIT:
					running = SDL_FALSE;
					break;
			}
		}

		/* do eui */
		if (eui_begin_sdl2(surface4))
		{
			/* clear */
			eui_clear(1);

			/* set alignment to center */
			eui_set_align(EUI_ALIGN_MIDDLE, EUI_ALIGN_MIDDLE);

			/* icon by susan kare, thank you <3 */
			pos.x = 0;
			pos.y = -48;
			eui_xbm(pos, 15, mac_width, mac_height, mac_bits);

			/* button */
			pos.x = 0;
			pos.y = 0;
			size.x = 128;
			size.y = 16;
			if (eui_button(pos, size, "My Cool Button", my_cool_button_callback, NULL))
			{
				/* icon by xorg */
				pos.x = 0;
				pos.y = 48;
				eui_xbm(pos, 0xD, xlogo64_width, xlogo64_height, xlogo64_bits);
			}

			/* text */
			pos.x = 0;
			pos.y = -20;
			eui_text(pos, 0xA, "My Cool Text");

			/* rect */
			pos.x = 0;
			pos.y = 0;
			size.x = 256;
			size.y = 192;
			eui_border_box(pos, size, 2, 0xE);

			/* end eui */
			eui_end();
		}

		/* blit to screen */
		SDL_BlitSurface(surface4, &blitrect, surface32, &blitrect);
		SDL_UpdateTexture(texture, NULL, surface32->pixels, surface32->pitch);
		SDL_RenderClear(renderer);
		SDL_RenderCopy(renderer, texture, NULL, NULL);
		SDL_RenderPresent(renderer);
	}

	/* quit */
	SDL_FreeSurface(surface4);
	SDL_FreeSurface(surface32);
	SDL_DestroyTexture(texture);
	SDL_DestroyRenderer(renderer);
	SDL_DestroyWindow(window);
	SDL_Quit();

	return 0;
}
